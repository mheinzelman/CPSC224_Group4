
public class LeaderboardNode {
	public int player;
	public int score;
	
	LeaderboardNode(int player){
		this.player = player;
		score = 0;
	}
	
	public int getPlayer() {
		return player;
	}
	
	public int getScore() {
		return player;
	}
	
	public void setPlayer(int playerIn) {
		player = playerIn;
	}
	
	public void setScore(int scoreIn) {
		score = scoreIn;
	}
}

